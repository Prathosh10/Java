package com.JAVA_DSA;

public class typeCasting {
    public static void main(String[]args){
        int a=10;
        byte b=(byte)a;
        float c=88;
        System.out.println("a="+a);
        System.out.println("b="+b);
        System.out.println("c="+c);
        a= (int)c;

        System.out.println("a="+a);
    }
}
